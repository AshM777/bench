const byId = new Map<string, ConnectorDefinition>();
for (const c of CONNECTOR_CATALOG) {
  if (byId.has(c.id)) throw new Error(`Duplicate connector id: ${c.id}`);
  byId.set(c.id, c);
}

export function getConnectorById(id: string): ConnectorDefinition | undefined {
  return byId.get(id);
}

export function listConnectorsByCategory(category: ConnectorCategory): ConnectorDefinition[] {
  return CONNECTOR_CATALOG.filter((c) => c.category === category);
}

export function groupedConnectors(): Map<ConnectorCategory, ConnectorDefinition[]> {
  const map = new Map<ConnectorCategory, ConnectorDefinition[]>();
  for (const cat of CONNECTOR_CATEGORY_ORDER) {
    map.set(cat, []);
  }
  for (const c of CONNECTOR_CATALOG) {
    map.get(c.category)!.push(c);
  }
  return map;
}
